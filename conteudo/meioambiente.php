<!-- Seção Reflorestamento -->
<section>
    <h2>Reflorestamento</h2>
    <img src="imagens/reflorestamento.jpg" alt="Imagem de reflorestamento" width="300">
    <p>
        O reflorestamento é o processo de replantio de áreas que foram desmatadas, seja para restaurar ecossistemas, proteger a biodiversidade, ou absorver carbono da atmosfera. Esse processo ajuda a combater a degradação do solo e a aumentar a retenção de água nas regiões desmatadas, contribuindo para a recuperação do clima e das condições ambientais.
    </p>
</section>

<hr>

<!-- Seção Desmatamento -->
<section>
    <h2>Desmatamento</h2>
    <img src="imagens/desmatamento.jpg" alt="Imagem de desmatamento" width="300">
    <p>
        O desmatamento é a remoção de árvores e vegetação de uma área, geralmente para fins agrícolas ou industriais. Esse processo pode causar perda de habitat, extinção de espécies e emissão de grandes quantidades de carbono, contribuindo para as mudanças climáticas. É um problema que ameaça a biodiversidade e a sustentabilidade de diversos ecossistemas.
    </p>
</section>

<hr>

<!-- Seção Queimadas -->
<section>
    <h2>Queimadas</h2>
    <img src="imagens/queimadas.jpg" alt="Imagem de queimadas" width="300">
    <p>
        Queimadas são incêndios geralmente causados pela ação humana, seja para limpar terrenos para cultivo ou por descuido. As queimadas destroem a vegetação, liberam gases tóxicos e aumentam o nível de poluição. Elas também afetam a qualidade do solo e podem contribuir para o aumento do efeito estufa, impactando o clima global.
    </p>
</section>
